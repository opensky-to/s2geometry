namespace OpenSky.S2Geometry.Extensions
{
    using System.Collections.Generic;

    /// -------------------------------------------------------------------------------------------------
    /// <summary>
    /// Doughnut coverage result.
    /// </summary>
    /// <remarks>
    /// sushi.at, 08/12/2021.
    /// </remarks>
    /// -------------------------------------------------------------------------------------------------
    public class DoughnutCoverage
    {
        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the exclude cells (inner circle).
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public List<S2CellId> ExcludeCells { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the exclude level.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public int ExcludeLevel { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the include cells (outer circle).
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public List<S2CellId> IncludeCells { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the include level.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public int IncludeLevel { get; set; }
    }
}